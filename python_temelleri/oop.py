# Object Oriented Programming   Nesne Tabanlı Programlama

# class
# instance (object)

lst1 = [1, 2, 3] # Burada tanımlanmış olan liste bir list classından türetilmiş
# bir instance'dır. 
# Bir liste tanımlandığında oluşturulmuş olan class'ın bir kopyasını oluşturmuş oluyorsun.
result = type(lst1)  
print(result)
lst2 = [1, 2, 3, 4]
result1 = type(lst2)
print(result1)

# lst1 ve lst2 aynı list classından üretilmiş olan birer kopyalar.
